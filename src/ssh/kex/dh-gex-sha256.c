/* Group table for default_select_group() */
static const struct {
	uint32_t           bits;
	const uint8_t     *p;
	size_t             p_len;
} rfc3526_groups[] = {
	{ 2048, rfc3526_grp14_p, sizeof(rfc3526_grp14_p) },
	{ 3072, rfc3526_grp15_p, sizeof(rfc3526_grp15_p) },
	{ 4096, rfc3526_grp16_p, sizeof(rfc3526_grp16_p) },
	{ 6144, rfc3526_grp17_p, sizeof(rfc3526_grp17_p) },
	{ 8192, rfc3526_grp18_p, sizeof(rfc3526_grp18_p) },
};
#define RFC3526_NGROUPS (sizeof(rfc3526_groups) / sizeof(rfc3526_groups[0]))

/*
 * Built-in group selector: pick the largest RFC 3526 group whose
 * bit size is within [min, max], preferring the one closest to
 * preferred.  All groups use generator g = 2.
 */
static int
default_select_group(uint32_t min, uint32_t preferred, uint32_t max,
    uint8_t **p_out, size_t *p_len,
    uint8_t **g_out, size_t *g_len, void *cbdata)
{
	(void)cbdata;

	/* Find the best group: closest to preferred, within [min, max]. */
	const uint8_t *best_p = NULL;
	size_t         best_len = 0;
	uint32_t       best_bits = 0;
	uint32_t       best_dist = UINT32_MAX;

	for (size_t i = 0; i < RFC3526_NGROUPS; i++) {
		uint32_t bits = rfc3526_groups[i].bits;

		if (bits < min || bits > max)
			continue;
		uint32_t dist = (bits >= preferred)
		    ? bits - preferred : preferred - bits;
		if (dist < best_dist ||
		    (dist == best_dist && bits > best_bits)) {
			best_p    = rfc3526_groups[i].p;
			best_len  = rfc3526_groups[i].p_len;
			best_bits = bits;
			best_dist = dist;
		}
	}
	if (best_p == NULL)
		return DSSH_ERROR_INIT;

	*p_out = malloc(best_len);
	if (*p_out == NULL)
		return DSSH_ERROR_ALLOC;
	memcpy(*p_out, best_p, best_len);
	*p_len = best_len;

	*g_out = malloc(1);
	if (*g_out == NULL) {
		free(*p_out);
		return DSSH_ERROR_ALLOC;
	}
	(*g_out)[0] = 2;
	*g_len = 1;

	return 0;
}

